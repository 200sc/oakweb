// Wordbreak specifies line breaking rules for non CJK scripts.
// Initial is normal. Normal does not define anything.
func Wordbreak(setting WordbreakProperty) CSSOption { return nil }

// WordbreakProperty defines what positions words are allowed
// to be broken on for non CJK scripts
type WordbreakProperty int

const (
	_ = iota // initial
	_        // inherit
	_        // normal
	_        // auto
	// WordbreakBreakAll allows line breaks between any letters
	WordbreakBreakAll WordbreakProperty = iota
	// WordbreakKeepAll forbids line breaks between letter pairs
	WordbreakKeepAll WordbreakProperty = iota
)

// Wordspacing specifies the distance in between words in text blocks.
// A setting of normal indicates .25em.
// Initial is normal.
func Wordspacing(setting int) CSSOption { return nil }

// WordspacingPx is equivalent to css for "word-spacing: ($length)px"
func WordspacingPx(length int) CSSOption { return nil }

// Wordwrap defines how words should be wrapped around new lines.
// A setting of normal indicates that they will only be broken at
// 'allowed' points, which (editorializing) I assume refers to in
// between words.
// Initial is normal.
func Wordwrap(setting WordwrapProperty) CSSOption { return nil }

// WordwrapProperty defines how words should break and be wrapped onto
// successive lines
type WordwrapProperty int

const (
	_ = iota // initial
	_        // inherit
	_        // normal
	_        // auto
	// WordwrapBreakWord allows unbreakable words to be broken
	WordwrapBreakWord WordwrapProperty = iota
)

